// Map: problemSlug -> available templates grouped by instance
export function getTemplatesForProblem(slug: string): ComplaintTemplate[] {
  return complaintTemplates
    .filter(t => t.problemSlug === slug)
    .sort((a, b) => a.escalationStep - b.escalationStep);
}

// Get all unique problem slugs that have templates
export function getAvailableProblemSlugs(): string[] {
  return [...new Set(complaintTemplates.map(t => t.problemSlug))];
}
